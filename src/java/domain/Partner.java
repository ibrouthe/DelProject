public class Partner {
    
    int parID;
    String parName;
    String parAdress;
    int parZIP;
    String parCity;
    int parPhone;
    String eMail;

    public int getParID() {
        return parID;
    }

    public void setParID(int parID) {
        this.parID = parID;
    }

    public String getParName() {
        return parName;
    }

    public void setParName(String parName) {
        this.parName = parName;
    }

    public String getParAdress() {
        return parAdress;
    }

    public void setParAdress(String parAdress) {
        this.parAdress = parAdress;
    }

    public int getParZIP() {
        return parZIP;
    }

    public void setParZIP(int parZIP) {
        this.parZIP = parZIP;
    }

    public String getParCity() {
        return parCity;
    }

    public void setParCity(String parCity) {
        this.parCity = parCity;
    }

    public int getParPhone() {
        return parPhone;
    }

    public void setParPhone(int parPhone) {
        this.parPhone = parPhone;
    }

    public String geteMail() {
        return eMail;
    }

    public void seteMail(String eMail) {
        this.eMail = eMail;
    }
    
    
    
}
